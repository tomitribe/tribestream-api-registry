package com.tomitribe.tribestream.registryng.entities;

import org.hibernate.envers.RevisionEntity;
import org.hibernate.envers.RevisionNumber;
import org.hibernate.envers.RevisionTimestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * The entity storing the Hibernate Envers revision history.
 */
@Entity
@Table(name = "REVISION")
@RevisionEntity(RevisionListener.class)
public class Revision {

    @Id
    @GeneratedValue
    @RevisionNumber
    @Column(name = "ID")
    private int id;

    @RevisionTimestamp
    @Column(name = "TIMESTAMP")
    private long timestamp;

    @Column(name = "USERNAME", length = 255, nullable = true)
    private String username;

    @Column(name = "USERCOMMENT", length = 4096, nullable = true)
    private String userComment;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUserComment() {
        return userComment;
    }

    public void setUserComment(String userComment) {
        this.userComment = userComment;
    }
}
